function seed() {
  console.log('🌱 Seeding database...');

  // Drop and recreate tables to ensure clean schema
  db.exec(`
    DROP TABLE IF EXISTS cities;
    DROP TABLE IF EXISTS countries;
    DROP TABLE IF EXISTS users;

    CREATE TABLE users (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      username TEXT UNIQUE NOT NULL,
      email TEXT UNIQUE NOT NULL,
      password TEXT NOT NULL,
      role TEXT DEFAULT 'user',
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );

    CREATE TABLE countries (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT UNIQUE NOT NULL,
      code TEXT UNIQUE NOT NULL,
      capital TEXT,
      continent TEXT,
      population INTEGER,
      area_km2 REAL,
      currency TEXT,
      language TEXT,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );

    CREATE TABLE cities (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL,
      country_code TEXT NOT NULL,
      population INTEGER,
      latitude REAL,
      longitude REAL,
      is_capital INTEGER DEFAULT 0,
      timezone TEXT,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      updated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (country_code) REFERENCES countries(code) ON DELETE CASCADE
    );

    CREATE INDEX idx_cities_country ON cities(country_code);
    CREATE INDEX idx_countries_continent ON countries(continent);
  `);
  console.log('✅ Tables recreated cleanly');

  // Disable FK checks during seeding to avoid constraint errors
  db.pragma('foreign_keys = OFF');

  // Admin user
  const adminPass = bcrypt.hashSync('admin123', 10);
  db.prepare(`
    INSERT OR IGNORE INTO users (username, email, password, role)
    VALUES ('admin', 'admin@example.com', ?, 'admin')
  `).run(adminPass);
  console.log('✅ Admin user created  (email: admin@example.com | password: admin123)');

  // Countries
  const insertCountry = db.prepare(`
    INSERT OR IGNORE INTO countries (name, code, capital, continent, population, area_km2, currency, language)
    VALUES (@name, @code, @capital, @continent, @population, @area_km2, @currency, @language)
  `);

  const insertManyCountries = db.transaction((list) => {
    for (const c of list) insertCountry.run(c);
  });

  insertManyCountries(countries);
  console.log(`✅ ${countries.length} countries inserted`);

  // Cities
  const insertCity = db.prepare(`
    INSERT OR IGNORE INTO cities (name, country_code, population, latitude, longitude, is_capital, timezone)
    VALUES (?, ?, ?, ?, ?, ?, ?)
  `);

  const insertManyCities = db.transaction((list) => {
    for (const c of list) insertCity.run(...c);
  });

  insertManyCities(cities);
  console.log(`✅ ${cities.length} cities inserted`);

  // Re-enable FK checks
  db.pragma('foreign_keys = ON');

  const total = db.prepare('SELECT COUNT(*) as n FROM cities').get().n;
  console.log(`\n🎉 Done! Total rows in cities table: ${total}`);
  console.log(`   Total rows in countries table: ${db.prepare('SELECT COUNT(*) as n FROM countries').get().n}`);
}

